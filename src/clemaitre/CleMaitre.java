    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed

        JFileChooser chooser = new JFileChooser("src");
        int returnVal = chooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            // captura selecciÃ³n del usuario
            String pathArchivo = chooser.getSelectedFile().getParent();
            String nombreArchivo = chooser.getSelectedFile().getName();
            String nombre = nombreArchivo.replace(".pass", "");
            try { // cargar el archivo como ObjectInputStream

                String sospechoso = this.calcularHash(nombre);
                System.out.println(sospechoso);
                String original = this.leerHash(nombre);
                System.out.println(original);
                if (!sospechoso.equals(original)) {
                    JOptionPane.showMessageDialog(this, "El archivo ha perdido su integridad", "Problema de archivo", JOptionPane.ERROR_MESSAGE);
                }
                this.user = cargarDatosUsuario(pathArchivo + "/" + nombreArchivo);
                new loginCleMaitre(user).setVisible(true);
                this.setVisible(false);
                JOptionPane.showMessageDialog(this, "Datos cargados Satisfactoriamente", "Informacion", JOptionPane.WARNING_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Problema de archivo", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

