    private void jTreeGruposValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_jTreeGruposValueChanged

        TreePath tp = evt.getNewLeadSelectionPath();

        if (tp != null) {

            System.out.println(tp.getLastPathComponent().toString());
            String nombrenodo = tp.getLastPathComponent().toString();
            seleccionarbol = nombrenodo;

            for (Grupo g : user.getGrupos()) {
                if (g.getNombre().equals(nombrenodo)) {

                    actualizarTabla(g);

                } else {

                    mirarHijos(g, nombrenodo);

                }
            }
        }
    }//GEN-LAST:event_jTreeGruposValueChanged

    private void mirarHijos(Grupo dato, String nombrenodo) {

        if (dato.getSubgrupo().isEmpty() == false) {

            for (Grupo T : dato.getSubgrupo()) {
                if (nombrenodo.equals(T.getNombre()) == false) {

                    mirarHijos(T, nombrenodo);
                } else {

                    actualizarTabla(T);

                }
            }

        }

    }

    private void actualizarTabla(Grupo p) {

        DefaultTableModel model = (DefaultTableModel) tablaEntradas.getModel();
        int filas = model.getRowCount();
        for (int i = filas - 1; i >= 0; i--) {//Limpiar la tabla antes de mostrar los valores de otro grupo
            model.removeRow(i);
        }
        reusar = p;
        for (Entrada e : p.getEntradas()) {
            Object[] fila = new Object[5];
            fila[0] = e.getNombreentrada();
            System.out.println(e.getNombreentrada());
            fila[1] = e.getUsuario();
            fila[2] = e.getURL();
            fila[3] = e.getClave();
            fila[4] = e.getNota();
            model.addRow(fila);

        }

    }

