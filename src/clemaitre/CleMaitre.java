    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String titulo = jTextField1.getText();
        String usuario = jTextField2.getText();
        String clave = jPasswordField1.getText();
        String confirmacion = jPasswordField2.getText();
        String URL = jTextField5.getText();
        String nota = jTextArea1.getText();
        Calendar creacion = GregorianCalendar.getInstance();
        Calendar modificacion = GregorianCalendar.getInstance();
        Calendar ultimoacceso = GregorianCalendar.getInstance();

        SimpleDateFormat curFormater = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss");
        String pegado = jTextField6.getText() + " " + jTextField7.getText();
        System.out.println(pegado);
        Date dateObj;
        Calendar expira = GregorianCalendar.getInstance();;
        try {
            dateObj = curFormater.parse(pegado);
            expira = GregorianCalendar.getInstance();
            expira.setTime(dateObj);

        } catch (ParseException ex) {
            Logger.getLogger(CleMaitre.class
                    .getName()).log(Level.SEVERE, null, ex);
        }

        /*
         ^                 # Inicio de String
         (?=.*[0-9])       # Al menos un numero
         (?=.*[a-z])       # Al menos una minuscula
         (?=.*[A-Z])       # Al menos una Mayuscula
         (?=.*[@#$%^&+-=]) # Al menos un caracter especial
         (?=\S+$)          # Sin espacios
         .{12,}            # Al menos 12 caracteres
         $                 # Final de String
         */
        String pattern = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%^&+-=])(?=\\S+$).{8,}$";
        if (jPasswordField1.getText().matches(pattern) && jPasswordField2.getText().matches(pattern) && jPasswordField1.getText().equals(jPasswordField2.getText())) {
            //Contraseña valida
            //ENCRIPTAR CON AES
            commitEntrada(titulo, usuario, clave, confirmacion, URL, nota, creacion, modificacion, ultimoacceso, expira);
        } else {
            //Contraseña invalida
            System.out.println("Contraseña No Válida");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void commitEntrada(String titulo, String usuario, String clave, String confirmacion, String URL, String nota, Calendar creacion, Calendar modificacion, Calendar ultimoacceso, Calendar expira) {
        {

            if (jTextField1.getText().isEmpty() == false && jTextField2.getText().isEmpty() == false && jPasswordField2.getText().isEmpty() == false
                    && jPasswordField1.getText().isEmpty() == false && jTextField5.getText().isEmpty() == false) {

                for (Grupo g : this.user.getGrupos()) {

                    if (g.getSubgrupo().isEmpty() != true) {
                        if (jComboBox1.getSelectedItem().equals(g.getNombre()) == false) {
                            try {
                                accesoNiveles(g);

                            } catch (ParseException ex) {
                                Logger.getLogger(CleMaitre.class
                                        .getName()).log(Level.SEVERE, null, ex);
                            }
                        } else {
                            Entrada e = new Entrada(titulo, usuario, clave, confirmacion, URL, nota, creacion, modificacion, ultimoacceso, expira);
                            g.addEntrada(e);
                        }
                    }
                }

            }

            jFrameAgregarEntrada.setVisible(false);
            limpiarFormulario();

        }
    }

    private void accesoNiveles(Grupo dato) throws ParseException {

        String titulo = jTextField1.getText();
        String usuario = jTextField2.getText();
        /// COLOCAR ENCRIPTACION CON AES ///
        String clave = jPasswordField1.getText();
        String confirmacion = jPasswordField2.getText();
        //PONER EXPRESION REGULAR //
        String URL = jTextField5.getText();
        String nota = jTextArea1.getText();
        Calendar creacion = GregorianCalendar.getInstance();
        Calendar modificacion = GregorianCalendar.getInstance();
        Calendar ultimoacceso = GregorianCalendar.getInstance();
        SimpleDateFormat curFormater = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss");
        Date dateObj = curFormater.parse(jTextField6.getText() + " " + jTextField7.getText());
        Calendar expira = GregorianCalendar.getInstance();
        expira.setTime(dateObj);

        for (Grupo T : dato.getSubgrupo()) {
            if (jComboBox1.getSelectedItem().equals(T.getNombre()) == false) {
                accesoNiveles(T);
            } else {
                Entrada e = new Entrada(titulo, usuario, clave, confirmacion, URL, nota, creacion, modificacion, ultimoacceso, expira);
                T.addEntrada(e);
            }
        }

    }

