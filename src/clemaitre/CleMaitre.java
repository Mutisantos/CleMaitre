        if (jPasswordField3.getText().matches(pattern) && jPasswordField4.getText().matches(pattern) && jPasswordField3.getText().equals(jPasswordField4.getText())) {
            //Contraseña valida
            //ENCRIPTAR CON AES
            commitEditarEntrada(titulo, usuario, clave, confirmacion, URL, nota, creacion, modificacion, ultimoacceso, expira);
        } else {
            //Contraseña invalida
            System.out.println("Contraseña No Válida");
        }
