    private void ManejoArbolGrupos() {
        DefaultTreeModel model = (DefaultTreeModel) jTreeGrupos.getModel();
        DefaultMutableTreeNode root = new DefaultMutableTreeNode("Todos los Grupos");
        model.setRoot(root);
        System.out.println("Entre en manejo Arbol");
        for (Grupo G : user.getGrupos()) {

            DefaultMutableTreeNode nodo = new DefaultMutableTreeNode(G.getNombre());
            System.out.println("General " + G.getNombre());
            if (!G.getSubgrupo().isEmpty()) {
                for (Grupo sub : G.getSubgrupo()) {
                    System.out.println("Especifico:" + sub.getNombre());
                    agregarNodo(sub, nodo, model);
                }
            }
            root.add(nodo);
            model.reload();
        }
        model.reload();
        jTreeGrupos = new JTree(model);
        add(jTreeGrupos);

    }

    private void agregarNodo(Grupo dato, DefaultMutableTreeNode root, DefaultTreeModel model) {
        DefaultMutableTreeNode nodo = new DefaultMutableTreeNode(dato.getNombre());
        for (Grupo T : dato.getSubgrupo()) {

            if (T.getSubgrupo().isEmpty() != true) {
                agregarNodo(T, nodo, model);
            }
        }
        root.add(nodo);
        model.reload();
    }

