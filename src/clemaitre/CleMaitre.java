    private void commitEditarEntrada(String titulo, String usuario, String clave, String confirmacion, String URL, String nota, Calendar creacion, Calendar modificacion, Calendar ultimoacceso, Calendar expira) {
        {

            if (jTextField3.getText().isEmpty() == false && jTextField4.getText().isEmpty() == false && jPasswordField3.getText().isEmpty() == false
                    && jPasswordField4.getText().isEmpty() == false && jTextField12.getText().isEmpty() == false) {

                for (Grupo g : this.user.getGrupos()) {

                    if (g.getSubgrupo().isEmpty() != true) {
                        if (jComboBox3.getSelectedItem().equals(g.getNombre()) == false) {
                            try {
                                accesoEditarNiveles(g);

                            } catch (ParseException ex) {
                                Logger.getLogger(CleMaitre.class
                                        .getName()).log(Level.SEVERE, null, ex);
                            }
                        } else {

                            Entrada e = new Entrada(titulo, usuario, clave, confirmacion, URL, nota, creacion, modificacion, ultimoacceso, expira);
                            g.deleteEntrada(entradaactual.getNombreentrada());
                            g.addEntrada(e);
                        }
                    }
                }

            }

            jFrameEditarEntrada.setVisible(false);
            limpiarFormulario();

        }
    }

    private void accesoEditarNiveles(Grupo dato) throws ParseException {

        String titulo = jTextField3.getText();
        String usuario = jTextField4.getText();
        String clave = jPasswordField3.getText();
        String confirmacion = jPasswordField4.getText();
        String URL = jTextField12.getText();
        String nota = jTextArea2.getText();
        Calendar creacion = GregorianCalendar.getInstance();
        Calendar modificacion = GregorianCalendar.getInstance();
        Calendar ultimoacceso = GregorianCalendar.getInstance();

        SimpleDateFormat curFormater = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss");
        Date dateObj = curFormater.parse(jTextField13.getText() + " " + jTextField14.getText());
        Calendar expira = GregorianCalendar.getInstance();
        expira.setTime(dateObj);

        for (Grupo T : dato.getSubgrupo()) {
            if (jComboBox3.getSelectedItem().equals(T.getNombre()) == false) {
                accesoEditarNiveles(T);
            } else {
                Entrada e = new Entrada(titulo, usuario, clave, confirmacion, URL, nota, creacion, modificacion, ultimoacceso, expira);
                T.deleteEntrada(entradaactual.getNombreentrada());
                T.addEntrada(e);
            }
        }

    }


