    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        /*
            ^                 # Inicio de String
            (?=.*[0-9])       # Al menos un numero
            (?=.*[a-z])       # Al menos una minuscula
            (?=.*[A-Z])       # Al menos una Mayuscula
            (?=.*[@#$%^&+-=]) # Al menos un caracter especial
            (?=\S+$)          # Sin espacios
            .{12,}            # Al menos 12 caracteres
            $                 # Final de String
        */
        String pattern = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%^&+-=])(?=\\S+$).{8,}$";
            if (jPasswordField1.getText().matches(pattern)) {
                //Contrase침a valida
                
                String encriptada = jPasswordField1.getText();
                String nombre = jTextField1.getText();
                this.setVisible(false);
                
                CleMaitre sesion = new CleMaitre(true,encriptada,nombre);
                System.out.println("PASO");
                
                sesion.setVisible(true);
            } else {
                //Contrase침a invalida
                System.out.println("Contrase침a No V치lida");
            }
  
        
    }//GEN-LAST:event_jButton1ActionPerformed

